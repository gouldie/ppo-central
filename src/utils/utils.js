export function mapNameToImg(name) {
  switch (name) {
    case 'Deep Viridian Forest':
      return 'deep-viridian-forest.png'
    case 'Turtle Cove':
      return 'turtle-cove.png'
    case 'Hellfire Cavern':
      return 'hellfire-cavern.png'
    default:
      return
  }
}

export function pokemonNameToMainImg(name) {
  switch (name) {
    case 'Bulbasaur':
      return 'bulbasaur-main.png'
    case 'Ivysaur':
      return 'ivysaur-main.png'
    case 'Venusaur':
      return 'venusaur-main.png'
    case 'Squirtle':
      return 'squirtle-main.png'
    case 'Charmander':
      return 'charmander-main.png'
    default:
      return
  }
}
